package cgc.models;

/**
 * Data for the vintage of geography for geographical lookup
 */
public class Vintage {
    /**
     * If a vintage is the default one used for geographical lookup
     */
    public boolean isDefault;
    /**
     * The ID representing a vintage
     */
    public String id;
    /**
     * The name of a vintage
     */
    public String vintageName;
    /**
     * The description of a vintage
     */
    public String vintageDescription;
}
