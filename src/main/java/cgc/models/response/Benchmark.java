package cgc.models.response;

/**
 * Data for the locator version to be searched
 */
public class Benchmark {
    /**
     * If a benchmark is the default one used for locations
     */
    public boolean isDefault;
    /**
     * Description of a benchmark
     */
    public String benchmarkDescription;
    /**
     * The ID representing a benchmark
     */
    public String id;
    /**
     * The name of a benchmark
     */
    public String benchmarkName;
}
